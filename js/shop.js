function purchaseItem(itemId) {
    const item = shopItems.find(i => i.id === itemId);
    
    if (!item) {
        console.error(`‚ùå Article ${itemId} introuvable`);
        return;
    }
    
    // V√©rifier si assez de points
    if (playerPoints < item.price) {
        if (typeof showMoonMessage === 'function') {
            showMoonMessage("üåô Pas assez de points ! Joue aux mini-jeux.", 3000);
        }
        return;
    }
    
    // D√©duire les points
    playerPoints -= item.price;
    localStorage.setItem('playerPoints', playerPoints);
    
    // Appliquer l'effet
    item.effect();
    
    // Mettre √† jour l'affichage
    updatePointsDisplay();
    renderShopItems();
    
    // Message de la Lune
    if (typeof moonMessageShopPurchase === 'function') {
        moonMessageShopPurchase(item.name);
    }
    
    console.log(`üõí Achet√© : ${item.name} (-${item.price} pts)`);
}

